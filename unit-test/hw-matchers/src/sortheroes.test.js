import sortHelthHeroes from './app.js';

test('Проверка сортировка по убыванию', ()=> {
  const input = [
    {name: 'мечник', health: 10},
    {name: 'маг', health: 100},
    {name: 'лучник', health: 80},
  ];
  const expected = [
    {name: 'маг', health: 100},
    {name: 'лучник', health: 80},
    {name: 'мечник', health: 10},
  ];
  const received = sortHelthHeroes(input);

  expect(received).toEqual(expected);
});